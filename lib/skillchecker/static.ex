defmodule Skillchecker.Static do
  @moduledoc """
  The Static context.
  """

  import Ecto.Query, warn: false
  alias Skillchecker.Repo

  alias Skillchecker.Static.Item
  alias Skillchecker.Static.Group

  def get_item_name(item_id) do
    result = Repo.get_by!(Item, eveid: item_id)
    result.name
  end

  def get_group_name(item_id) do
    item = Repo.get_by!(Item, eveid: item_id)
    result = Repo.get_by!(Group, groupid: item.groupid)
    result.name
  end

  def find_item_eveid(name) do
    result =
      Item
      |> where(name: ^name)
      |> where(fragment("skill_multiplier IS NOT NULL"))
      |> Repo.one

    if result do
      result.eveid
    end
  end

  def get_skill_multiplier(skill_id) do
    result = Repo.get_by!(Item, eveid: skill_id)
    result.skill_multiplier
  end

  @doc """
  Returns the list of static_items.

  ## Examples

      iex> list_static_items()
      [%Item{}, ...]

  """
  def list_static_items do
    Repo.all(Item)
  end

  @doc """
  Gets a single item.

  Raises `Ecto.NoResultsError` if the Item does not exist.

  ## Examples

      iex> get_item!(123)
      %Item{}

      iex> get_item!(456)
      ** (Ecto.NoResultsError)

  """
  def get_item!(id), do: Repo.get!(Item, id)

  @doc """
  Creates a item.

  ## Examples

      iex> create_item(%{field: value})
      {:ok, %Item{}}

      iex> create_item(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_item(attrs \\ %{}) do
    %Item{}
    |> Item.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a item.

  ## Examples

      iex> update_item(item, %{field: new_value})
      {:ok, %Item{}}

      iex> update_item(item, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_item(%Item{} = item, attrs) do
    item
    |> Item.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a item.

  ## Examples

      iex> delete_item(item)
      {:ok, %Item{}}

      iex> delete_item(item)
      {:error, %Ecto.Changeset{}}

  """
  def delete_item(%Item{} = item) do
    Repo.delete(item)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking item changes.

  ## Examples

      iex> change_item(item)
      %Ecto.Changeset{data: %Item{}}

  """
  def change_item(%Item{} = item, attrs \\ %{}) do
    Item.changeset(item, attrs)
  end

